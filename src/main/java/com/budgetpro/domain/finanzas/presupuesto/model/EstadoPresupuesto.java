package com.budgetpro.domain.finanzas.presupuesto.model;

/**
 * Enum que representa los estados posibles de un Presupuesto.
 * 
 * Estados válidos:
 * - EN_EDICION: Presupuesto en creación/modificación
 * - APROBADO: Presupuesto aprobado y listo para uso
 */
public enum EstadoPresupuesto {
    EN_EDICION,
    APROBADO
}
