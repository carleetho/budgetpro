package com.budgetpro.domain.finanzas.consumo.model;

/**
 * Enum que representa el tipo de consumo presupuestal.
 */
public enum TipoConsumo {
    /**
     * Consumo por compra de materiales/recursos.
     */
    COMPRA,
    
    /**
     * Consumo por planilla de mano de obra.
     */
    PLANILLA,
    
    /**
     * Consumo por otros conceptos.
     */
    OTROS
}
