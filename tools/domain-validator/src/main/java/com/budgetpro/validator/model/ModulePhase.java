package com.budgetpro.validator.model;

/**
 * Fase de desarrollo del módulo según el roadmap canónico.
 */
public enum ModulePhase {
    /**
     * Fase 1: Establecimiento de baseline (Proyecto, Presupuesto, Tiempo)
     */
    FOUNDATION,
    
    /**
     * Fase 2: Ejecución y consumo del baseline (Compras, Inventarios, RRHH, Estimación)
     */
    EXECUTION,
    
    /**
     * Fase 3: Análisis y medición de desempeño (EVM, Cambios, Alertas)
     */
    ANALYSIS
}
