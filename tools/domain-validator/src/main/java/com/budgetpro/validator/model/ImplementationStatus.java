package com.budgetpro.validator.model;

/**
 * Estado de implementación de un módulo.
 */
public enum ImplementationStatus {
    /**
     * Módulo no iniciado.
     */
    NOT_STARTED,
    
    /**
     * Módulo en progreso (parcialmente implementado).
     */
    IN_PROGRESS,
    
    /**
     * Módulo completamente implementado.
     */
    COMPLETE
}
