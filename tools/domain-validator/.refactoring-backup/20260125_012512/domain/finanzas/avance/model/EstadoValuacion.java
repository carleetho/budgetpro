package com.budgetpro.domain.finanzas.avance.model;

/**
 * Enum que representa los estados posibles de una Valuacion.
 */
public enum EstadoValuacion {
    /**
     * Valuación en borrador (puede modificarse).
     */
    BORRADOR,

    /**
     * Valuación aprobada (no puede modificarse).
     */
    APROBADA
}
